package com.Fundamentals.loops;
/*
16. Write a program to print the numbers from 10 to 1 in reverse order using a do-while loop.

 */

public class Loop16 {
    public static void main(String args[]){
        int i=10;
        do {
            System.out.println(i);
            i--;

        }while(i>0);
    }
}
